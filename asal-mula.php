							<p align="justify">Rawalitu Parking didirikan sejak tahun 2001 di Jakarta Indonesia . Rawalitu Parking kini telah berkembang dan menjadi perusahaan penyedia jasa pelayanan pengelolaan perparkiran terbesar di Indonesia. Rawalitu Parking menjadi seperti saat ini karena keinginan untuk selalu menjaga dan memperkuat hubungan kemitraan melalui setiap aspek pelayanan operasional kami</p>
<p align="justify"><img src="images/logo3.jpg" width="250" height="400" align="right" style="margin:0 0 4px 15px;">Di Indonesia, Rawalitu Parking telah melayani negeri ini sejak tahun 2001 dan telah memiliki lokasi parkir dalam operasional sebanyak 900 lokasi yang tersebar di beberapa kota besar di Indonesia, yakni Jabodetabek, Bandung, Surabaya, Yogyakarta, Batam, Medan, Manado, Pekanbaru, Padang, Palembang, Makassar, Lampung, Balikpapan, Bali, Semarang, Solo, Jambi, Binjai dan Gorontalo dengan total pengelolaan lebih dari satu juta petak parkir dan didukung oleh lebih dari 15.000 putra putri Indonesia terpilih & terlatih</p>
<p align="justify">Salah satu kunci sukses Rawalitu Parking hari ini adalah bahwa Rawalitu Parking menggabungkan teknologi - teknologi terbaru terhadap semua aspek manajemen perparkiran untuk menjadikan setiap pemilik properti dan para pengguna jasa parkir menjadi satu tanpa adanya batasan yang digabungkan dalam suatu program dan fasilitas pelayanan. Dan Rawalitu Parking merupakan satu-satunya perusahaan pengelola jasa perparkiran yang meraih sertifikat ISO 9001:2008 (Systems and Services Certification) untuk Car Park Management Systems, yang kini telah beranjak ke ISO 9001:2015.</p>						</div>
